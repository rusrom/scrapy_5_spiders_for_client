    def parse(self, response):
        meta_data = {}
        meta_data['street'] = response.xpath('//div[contains(@class, "office-locations-header")]/span[@itemprop="streetAddress"]/text()').extract_first()
        meta_data['locality'] = response.xpath('//div[contains(@class, "office-locations-header")]/span[@itemprop="addressLocality"]/text()').extract_first()
        meta_data['region'] = response.xpath('//div[contains(@class, "office-locations-header")]/span[@itemprop="addressRegion"]/text()').extract_first()
        meta_data['postal_code'] = response.xpath('//div[contains(@class, "office-locations-header")]/span[@itemprop="postalCode"]/text()').extract_first()

        meetme_url = response.url.replace('Locations', 'MeetMe')
        yield scrapy.Request(meetme_url, callback=self.parse_lead, meta=meta_data)

    def parse_lead(self, response):
        lead_data = {}

        lead_data['street'] = response.meta['street']
        lead_data['locality'] = response.meta['locality']
        lead_data['region'] = response.meta['region']
        lead_data['postal_code'] = response.meta['postal_code']

        lead_data['name'] = response.xpath('//h2[@itemprop="name"]/text()').extract_first()

        designations = response.xpath('string(//li[@class="agent-meet-me-designations"])').extract_first()
        if designations:
            lead_data['designations'] = designations.strip()
        else:
            lead_data['designations'] = ''

        itemscope = response.xpath('string(//li[@itemscope="itemscope"]/h3)').extract_first()
        if itemscope:
            lead_data['itemscope'] = itemscope.strip()
        else:
            lead_data['itemscope'] = ''

        lead_data['phone'] = response.xpath('//a[contains(@href, "tel")]/text()').extract_first()

        lead_data['fax'] = response.xpath('//li/span[@itemprop="faxNumber"]/text()').extract_first()
        lead_data['email'] = response.xpath('//a[contains(@href, "mailto:")]/text()').extract_first()

        yield lead_data
